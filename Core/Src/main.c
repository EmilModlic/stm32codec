void TAC5112_Debug_ShowByte(uint8_t value)
  {

      HAL_GPIO_WritePin(GPIOA, LEDG_Pin, 0);
      HAL_Delay(1000);
      HAL_GPIO_WritePin(GPIOA, LEDG_Pin, 1);
      HAL_Delay(300);

      for(int i = 7; i >= 0; i--)
      {
    	  HAL_GPIO_WritePin(GPIOA, LEDG_Pin, 1);
    	  HAL_GPIO_WritePin(GPIOB, LEDB_Pin, 1);
    	  HAL_GPIO_WritePin(GPIOC, LEDR_Pin, 1);

    	  if(value & (1 << i))
    	  {
    		  HAL_GPIO_WritePin(GPIOB, LEDB_Pin, 0);
    		  HAL_Delay(400);
    		  HAL_GPIO_WritePin(GPIOB, LEDB_Pin, 1);
    	  }
    	  else
    	  {
    		  HAL_GPIO_WritePin(GPIOC, LEDR_Pin, 0);
    		  HAL_Delay(400);
    		  HAL_GPIO_WritePin(GPIOC, LEDR_Pin, 1);
    	  }
    	  HAL_Delay(400);
      }

      HAL_Delay(1500);
  }
